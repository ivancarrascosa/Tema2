package boletin5;

public class Ejercicio1 {

	public static void main(String[] args) {
		// Creo una variable para guardar el número en cada momento 
		int num = 1;
		//Con un do while hago print del número y le sumo uno
		do {
			System.out.println(num);
			num++;
		} while (num<=20);

	}

}
